import os, sys

def get_connection():
    """Connect to Turso — tries Streamlit secrets first, then env vars."""
    try:
        import streamlit as st
        import libsql_experimental as libsql
        return libsql.connect(st.secrets["turso"]["url"], auth_token=st.secrets["turso"]["token"])
    except Exception:
        pass
    import libsql_experimental as libsql
    url = os.environ.get("TURSO_URL")
    token = os.environ.get("TURSO_TOKEN")
    if not url or not token:
        print("Set TURSO_URL and TURSO_TOKEN environment variables, or run via Streamlit.")
        sys.exit(1)
    return libsql.connect(url, auth_token=token)

